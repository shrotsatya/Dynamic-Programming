    public static int countSquares(int[][] matrix) {
        // if the matrix is empty, return 0
        if (matrix.length == 0 || matrix[0].length == 0) {
            return 0;
        }

        // create lookup table to store number of squares
        int[][] lookupTable;
        lookupTable = new int[matrix.length][];
        for (int i = 0; i < matrix.length; i++) {
            lookupTable[i] = new int[matrix[0].length];
            for (int j = 0; j < matrix[0].length; j++) {
                lookupTable[i][j] = 0;
            }
        }
        int result = 0;

        // copy first row and column of input matrix to lookup table
        for (int i = 0; i < matrix.length; i++) {
            lookupTable[i][0] = matrix[i][0];
        }
        for (int i = 0; i < matrix[0].length; i++) {
            lookupTable[0][i] = matrix[0][i];
        }

        // iterate over the matrix and store the count od squares in lookupTable
        for (int i = 1; i < matrix.length; i++) {
            for (int j = 1; j < matrix[0].length; j++) {
                // if matrix[i][j] is equal to 0
                if (matrix[i][j] == 0) {
                    continue;
                }

                // there is at least one square submatrix at this location, hence the + 1
                // in addition, find the minimum number of square submatrices
                // whose bottom-right corner is one of the neighbours of this location.
                lookupTable[i][j] = 1
                        + Math.min(lookupTable[i - 1][j], Math.min(lookupTable[i][j - 1], lookupTable[i - 1][j - 1]));
            }
        }

        // sum up the values in the lookupTable to get the count of square submatrices
        for (int i = 0; i < lookupTable.length; i++) {
            for (int j = 0; j < lookupTable[0].length; j++) {
                result += lookupTable[i][j];
            }
        }

        return result;
    }

